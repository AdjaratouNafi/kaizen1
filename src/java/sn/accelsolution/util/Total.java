public class Total {
    private String accord;
    private String acompte;
    private String acompteTTC;
    
    public Total(){
        
    }

    public String getAccord() {
        return accord;
    }

    public void setAccord(String accord) {
        this.accord = accord;
    }

    public String getAcompte() {
        return acompte;
    }

    public void setAcompte(String acompte) {
        this.acompte = acompte;
    }

    public String getAcompteTTC() {
        return acompteTTC;
    }

    public void setAcompteTTC(String acompteTTC) {
        this.acompteTTC = acompteTTC;
    }
    
    
}
