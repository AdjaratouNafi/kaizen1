package sn.accelsolution.util;

/**
 *
 * @author DV7
 */
public class UtilListItemChantierData {
    
    private String designation;
    private String ref;
    private String unite;
    private String quantite;
    private String qtLivree;
    private String execution;
    
    public UtilListItemChantierData(){
        
    }

    public String getDesignation() {
        return designation;
    }

    public void setDesignation(String designation) {
        this.designation = designation;
    }

    public String getRef() {
        return ref;
    }

    public void setRef(String ref) {
        this.ref = ref;
    }

    public String getUnite() {
        return unite;
    }

    public void setUnite(String unite) {
        this.unite = unite;
    }

    public String getQuantite() {
        return quantite;
    }

    public void setQuantite(String quantite) {
        this.quantite = quantite;
    }

    public String getQtLivree() {
        return qtLivree;
    }

    public void setQtLivree(String qtLivree) {
        this.qtLivree = qtLivree;
    }

    public String getExecution() {
        return execution;
    }

    public void setExecution(String execution) {
        this.execution = execution;
    }
    
    
}
